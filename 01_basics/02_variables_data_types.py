# Program 2: Variables and Data Types
# This program demonstrates variables of different types
# String variable
name = "Krishna"
# Integer variable
age = 21
# Float variable
height = 5.6
# Boolean variable
is_student = True

# Printing values
print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Is Student:", is_student)

# Print the type of each variable
print("\nData Types:")
print("Type of name:", type(name))
print("Type of age:", type(age))
print("Type of height:", type(height))
print("Type of is_student:", type(is_student))
